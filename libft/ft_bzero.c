#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*s_cpy;

	s_cpy = s;
	i = 0;
	while (i < n)
	{
		s_cpy[i] = 0;
		i++;
	}
	s = s_cpy;
}
